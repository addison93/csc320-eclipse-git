package name_and_address;

public class name_and_address {
	public static void main(String[] args) {
		String nameFirst = "Elliot";
		String nameLast = "Alderson";
		String addressStreet = "217 E Broadway";
		String addressCity = "New York, NY";
		String addressZip = "10002";
		
		System.out.println(nameFirst + " " + nameLast);
		System.out.println(addressStreet);
		System.out.println(addressCity + " " + addressZip);
				
	}
}
